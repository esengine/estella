    def _gen_embind(self) -> List[str]:
        return [
            '}  // anonymous namespace',
            '',
            '// ── Embind Registration ──',
            '',
            'EMSCRIPTEN_BINDINGS(esengine_editor_api) {',
            '    emscripten::function("editor_getComponentNames", &editor_getComponentNames);',
            '    emscripten::function("editor_getComponentSchema", &editor_getComponentSchema);',
            '',
            '    emscripten::function("editor_addComponent", &editor_addComponent);',
            '    emscripten::function("editor_removeComponent", &editor_removeComponent);',
            '    emscripten::function("editor_hasComponent", &editor_hasComponent);',
            '    emscripten::function("editor_getComponents", &editor_getComponents);',
            '',
            '    emscripten::function("editor_setFloat", &editor_setFloat);',
            '    emscripten::function("editor_getFloat", &editor_getFloat);',
            '    emscripten::function("editor_setInt", &editor_setInt);',
            '    emscripten::function("editor_getInt", &editor_getInt);',
            '    emscripten::function("editor_setBool", &editor_setBool);',
            '    emscripten::function("editor_getBool", &editor_getBool);',
            '',
            '    emscripten::register_vector<std::string>("VectorString");',
            '}',
        ]
